import os
import re
import sys

def amalgamate(root_file, out_file, include_paths):
    processed_files = set()
    std_includes = set()
    out_lines = []

    # Include guard for the amalgamated header
    out_lines.append("#ifndef CJSONX_SINGLE_HEADER_H\n")
    out_lines.append("#define CJSONX_SINGLE_HEADER_H\n\n")
    out_lines.append("// Amalgamated single-header version of cjsonx\n")
    out_lines.append("// Do not edit this file directly. Edit the source files in include/ and run scripts/amalgamate.py\n\n")

    def process_file(filepath):
        if filepath in processed_files:
            return
        processed_files.add(filepath)

        with open(filepath, 'r', encoding='utf-8') as f:
            lines = f.readlines()

        start_idx = 0
        if filepath != root_file:
            if len(lines) > 0 and (lines[0].strip().startswith("/**") or lines[0].strip().startswith("/*")):
                for idx, line in enumerate(lines):
                    if "*/" in line:
                        start_idx = idx + 1
                        break

        for line in lines[start_idx:]:
            # Strip #pragma once as it's not needed/wanted in amalgamated headers
            if line.strip() == "#pragma once":
                continue

            # Handle standard library includes
            std_match = re.match(r'^\s*#include\s+<([^>]+)>', line)
            if std_match:
                std_inc = std_match.group(0)
                if std_inc not in std_includes:
                    std_includes.add(std_inc)
                    out_lines.append(line)
                continue

            # Handle local includes
            local_match = re.match(r'^\s*#include\s+"([^"]+)"', line)
            if local_match:
                inc_name = local_match.group(1)
                found = False
                for search_path in include_paths:
                    candidate = os.path.join(search_path, inc_name)
                    if os.path.exists(candidate):
                        if candidate not in processed_files:
                            process_file(candidate)
                        found = True
                        break
                if not found:
                    print(f"Warning: Could not find included file: {inc_name}")
                continue
            
            # Pragma once is handled by processed_files, but we can also strip standard include guards if we want
            # We'll just leave them since they are harmless and sometimes needed if extracted.
            out_lines.append(line)

    process_file(root_file)
    
    out_lines.append("\n#endif // CJSONX_SINGLE_HEADER_H\n")

    with open(out_file, 'w', encoding='utf-8') as f:
        f.writelines(out_lines)
    
    print(f"Amalgamation complete: {out_file}")

if __name__ == "__main__":
    include_dirs = ["include"]
    root_header = "include/cjsonx.h"
    output_header = "single_include/cjsonx.h"
    
    if not os.path.exists("include"):
        print("Please run from the root of the project")
        sys.exit(1)
        
    amalgamate(root_header, output_header, include_dirs)
